package Actividad11;


public class Circal {

     
    public double getArea(double a){
              
        double area = Math.PI*Math.pow(a, 2);
        
        return area;
    }
    
    public double getPerimeter(double a){
        
        double perimeter = 2*Math.PI*a;

        return perimeter;
    }
    
}
